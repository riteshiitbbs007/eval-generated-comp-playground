import { LightningElement } from 'lwc';

export default class Gallery extends LightningElement {
  components = [];
  noteCounts = {};
  loading = true;
  error = null;
  searchTerm = '';

  async connectedCallback() {
    try {
      await Promise.all([
        this.loadComponents(),
        this.loadNoteCounts()
      ]);
    } catch (err) {
      this.error = err.message;
      console.error('Error loading components:', err);
    } finally {
      this.loading = false;
    }
  }

  async loadNoteCounts() {
    try {
      const response = await fetch('/api/notes/counts');
      if (response.ok) {
        this.noteCounts = await response.json();
      }
    } catch (err) {
      console.warn('Could not load note counts:', err);
      // Don't fail the whole page if note counts fail
    }
  }

  async loadComponents() {
    try {
      // Fetch component manifest dynamically generated by build script
      const manifestResponse = await fetch('/generated/components.json');
      if (!manifestResponse.ok) {
        throw new Error('Could not load component manifest');
      }

      const manifest = await manifestResponse.json();
      const componentsToLoad = manifest.components || [];

      console.log(`ðŸ“¦ Loading ${componentsToLoad.length} components from manifest`);

      const loadedComponents = [];

      for (const componentName of componentsToLoad) {
        try {
          // Load from generated/c/{componentName}/metadata.json
          const response = await fetch(`/generated/c/${componentName}/metadata.json`);
          if (response.ok) {
            const metadata = await response.json();

            // Auto-detect screenshots if not in metadata
            if (!metadata.screenshotUrls) {
              metadata.screenshotUrls = {
                desktop: `/generated/c/${componentName}/screenshots/desktop.png`
              };
            }

            // Add readiness class for color coding
            metadata.readinessClass = this.getReadinessClass(metadata.scores?.overall || 0);

            loadedComponents.push(metadata);
          }
        } catch (err) {
          console.warn(`Could not load ${componentName}:`, err.message);
        }
      }

      this.components = loadedComponents;

      // Sort by timestamp (newest first)
      this.components.sort((a, b) => new Date(b.timestamp) - new Date(a.timestamp));
    } catch (err) {
      console.error('Error loading components:', err);
      throw err;
    }
  }

  getReadinessClass(score) {
    if (score >= 2.5) return 'score-production';
    if (score >= 2.0) return 'score-prototype';
    return 'score-draft';
  }

  get hasComponents() {
    return this.components.length > 0;
  }

  get filteredComponents() {
    if (!this.searchTerm) {
      return this.components;
    }
    const searchLower = this.searchTerm.toLowerCase();
    return this.components.filter(comp =>
      comp.componentName.toLowerCase().includes(searchLower)
    );
  }

  get componentCount() {
    return this.filteredComponents.length;
  }

  get formattedComponents() {
    return this.filteredComponents.map(comp => {
      const date = new Date(comp.timestamp);
      const formattedDate = `${String(date.getDate()).padStart(2, '0')}/${String(date.getMonth() + 1).padStart(2, '0')}/${date.getFullYear()}`;
      const noteCount = this.noteCounts[comp.componentName] || 0;

      const notesLabel = noteCount === 1 ? 'note' : 'notes';

      return {
        ...comp,
        formattedDate,
        formattedTimestamp: new Date(comp.timestamp).toLocaleString(),
        formattedScore: comp.scores?.overall?.toFixed(2) || 'N/A',
        formattedSldsScore: comp.scores?.slds_linter?.toFixed(2) || 'N/A',
        formattedTokens: comp.tokenUsage?.totalTokens?.toLocaleString() || 'N/A',
        formattedCost: comp.cost?.totalCost ? `$${comp.cost.totalCost.toFixed(4)}` : 'N/A',
        noteCount,
        hasNotes: noteCount > 0,
        notesLabel,
        notesTooltip: `${noteCount} ${notesLabel}`
      };
    });
  }

  handleSearch(event) {
    this.searchTerm = event.target.value;
  }

  handleComponentSelect(event) {
    const componentName = event.target.dataset.name;
    if (componentName) {
      this.dispatchEvent(new CustomEvent('componentselect', {
        detail: { componentName },
        bubbles: true,
        composed: true
      }));
    }
  }
}
